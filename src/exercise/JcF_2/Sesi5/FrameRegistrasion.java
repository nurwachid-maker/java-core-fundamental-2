    private boolean checkUserId(JTextField textFieldIn) {
        byte huruf = 0;
        byte other = 0;
        char[] digits = textFieldIn.getText().toCharArray();
        for (byte nn = 0; nn < textFieldIn.getText().length(); nn++) {
            if (Character.isLetter(digits[nn])) {
                huruf++;
            } else {
                other++;
            }
        }

        if (other > 0) {
            JOptionPane.showMessageDialog(this, "UserId harus terdiri dari huruf");
        } else {
            if (huruf >= 8) {
                return (true);
            } else {

                JOptionPane.showMessageDialog(this, "UserId harus terdiri dari minimal 8 huruf");
            }
        }
        return (false);
    }

