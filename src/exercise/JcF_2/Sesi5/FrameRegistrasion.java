    private boolean checkPassWord(JTextField textFieldIn) {
        byte hurufKecil = 0;
        byte hurufBesar = 0;
        byte angka = 0;

        char[] digits = textFieldIn.getText().toCharArray();
        for (byte nn = 0; nn < textFieldIn.getText().length(); nn++) {
            if (Character.isLetter(digits[nn])) {
                if (Character.isLowerCase(digits[nn])) {
                    hurufKecil++;
                } else {
                    hurufBesar++;
                }
            } else {
                angka++;
            }
        }
        if (hurufKecil >= 3) {
            if (hurufBesar >= 1) {
                if (hurufKecil + hurufBesar >= 8) {
                    if (angka == 1) {
                        return (true);
                    } else {
                        JOptionPane.showMessageDialog(this, "Password harus terdiri minimal 1 digit angka");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Password harus terdiri minimal 8 digit huruf");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Password harus terdiri minimal 1 digit huruf kapital");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Password harus terdiri minimal 3 digit huruf kecil");
        }
        return (false);
    }

